class Solution:
    """
    反转一个 32 位整数的二进制位。
    我们将 n 视为一个 32 位的无符号整数。
    """
    def reverseBits(self, n: int) -> int:
        
        # 1. 初始化结果为 0
        res = 0
        
        # 2. 确保只处理 n 的低 32 位 (模拟无符号 32 位整数)
        # 在 Python 中，位操作默认是无限精度的，但 n & 0xFFFFFFFF 确保我们只关注低 32 位。
        # 0xFFFFFFFF 是 32 个 1。
        n_32bit = n & 0xFFFFFFFF
        
        # 3. 循环 32 次，因为必须处理满 32 位
        for i in range(32):
            
            # 步骤 1: 结果往左移动一位 (res <<= 1)
            # 相当于在 res 的最右端添加了一个 0，准备接收 n 的下一位
            res <<= 1
            
            # 步骤 2: 检查 n_32bit 的当前最低位是否为 1
            # n_32bit & 1 == 1
            if n_32bit & 1:
                # 如果是 1，则将 res 的最低位设置为 1 (res += 1)
                res += 1
                
            # 步骤 3: n_32bit 逻辑右移一位 (n_32bit >>= 1)
            # 准备检查 n_32bit 的下一位
            # 因为 n_32bit 是非负数 (n & 0xFFFFFFFF)，所以 Python 的 >> 实现了逻辑右移的效果
            n_32bit >>= 1
            
        # 最终返回的结果 res 可能会超过 32 位，
        # 为了符合 32 位无符号整数的语境，我们再次使用 & 0xFFFFFFFF 确保结果是 32 位的。
        return res & 0xFFFFFFFF

